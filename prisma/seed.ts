async function main() {
  console.log("🌱 Seeding database...");

  // Upsert top-level subjects (level 1)
  const createdSubjects: Record<string, string> = {};
  for (const subject of subjects) {
    const created = await prisma.subject.upsert({
      where: { slug: subject.slug },
      update: {
        name: subject.name,
        nameZh: subject.nameZh,
        color: subject.color,
        icon: subject.icon,
        sortOrder: subject.sortOrder,
        parentId: null,
      },
      create: subject,
    });
    createdSubjects[subject.slug] = created.id;
  }

  // Upsert sub-disciplines (level 2)
  for (const sub of subSubjects) {
    const { parentSlug, ...data } = sub;
    const parentId = createdSubjects[parentSlug];
    if (!parentId) {
      console.warn(`  ⚠ Parent not found for ${data.slug} (parent: ${parentSlug})`);
      continue;
    }
    await prisma.subject.upsert({
      where: { slug: data.slug },
      update: {
        name: data.name,
        nameZh: data.nameZh,
        color: data.color,
        icon: data.icon,
        sortOrder: data.sortOrder,
        parentId,
      },
      create: {
        ...data,
        parentId,
      },
    });
  }

  // Remove old subjects that are no longer in the standard
  const validSlugs = [
    ...subjects.map((s) => s.slug),
    ...subSubjects.map((s) => s.slug),
  ];

  // Migration map: old slug → new slug (for subjects with linked data)
  const migrationMap: Record<string, string> = {
    "computer-science": "520",
    "cs": "520",
    "biology": "180",
    "bio": "180",
    "physics": "140",
    "chemistry": "150",
    "mathematics": "110",
    "math": "110",
    "medicine": "310",
    "economics": "790",
    "psychology": "190",
    "engineering": "410",
    "humanities": "750",
    "law": "820",
    "education": "880",
    "art": "760",
    "history": "770",
    "philosophy": "720",
    "sociology": "840",
    "linguistics": "740",
    "political-science": "810",
    "astronomy": "160",
    "earth-science": "170",
    "agriculture": "210",
  };

  const obsoleteSubjects = await prisma.subject.findMany({
    where: { slug: { notIn: validSlugs } },
    select: {
      id: true,
      slug: true,
      nameZh: true,
      _count: { select: { posts: true, rooms: true } },
    },
  });

  for (const obs of obsoleteSubjects) {
    const targetSlug = migrationMap[obs.slug];
    const hasData = obs._count.posts > 0 || obs._count.rooms > 0;

    if (hasData && targetSlug) {
      // Migrate linked data to the new subject
      const target = await prisma.subject.findUnique({
        where: { slug: targetSlug },
        select: { id: true },
      });
      if (target) {
        if (obs._count.posts > 0) {
          await prisma.post.updateMany({
            where: { subjectId: obs.id },
            data: { subjectId: target.id },
          });
        }
        if (obs._count.rooms > 0) {
          await prisma.room.updateMany({
            where: { subjectId: obs.id },
            data: { subjectId: target.id },
          });
        }
        console.log(`  📦 Migrated ${obs._count.posts} posts + ${obs._count.rooms} rooms from "${obs.nameZh}" (${obs.slug}) → ${targetSlug}`);
      }
    }

    // Now safe to delete (either no data, or data was migrated)
    await prisma.subject.delete({ where: { id: obs.id } });
  }

  if (obsoleteSubjects.length > 0) {
    console.log(`  🗑 Removed ${obsoleteSubjects.length} obsolete subjects`);
  }

  // Create tags
  for (const tagName of tags) {
    await prisma.tag.upsert({
      where: { name: tagName },
      update: {},
      create: { name: tagName },
    });
  }

  // Create subject accounts (one per level-1 subject)
  await seedSubjectAccounts();

  console.log("✅ Seeding complete!");
  console.log(`  - ${subjects.length} level-1 subjects`);
  console.log(`  - ${subSubjects.length} level-2 sub-disciplines`);
  console.log(`  - ${tags.length} tags`);
  console.log(`  - ${subjects.length} subject accounts`);
}

// --- Subject Account Seeding ---

async function seedSubjectAccounts() {
  console.log("\n🎓 Creating subject accounts...");
  const passwordHash = await bcrypt.hash("Subject@2024", 12);

  for (const subject of subjects) {
    await prisma.user.upsert({
      where: { email: `subject-${subject.slug}@academic.local` },
      update: {},
      create: {
        email: `subject-${subject.slug}@academic.local`,
        username: `subject-${subject.slug}`,
        name: subject.nameZh,
        displayName: subject.nameZh,
        passwordHash,
        image: `https://api.dicebear.com/7.x/shapes/svg?seed=${subject.slug}`,
        avatar: `https://api.dicebear.com/7.x/shapes/svg?seed=${subject.slug}`,
        bio: `${subject.nameZh}学科官方账号`,
        role: "USER",
      },
    });
  }

  console.log(`  ✅ ${subjects.length} subject accounts ready`);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
